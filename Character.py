class Character:
    def __init__(self, character, car):
        self.character= character
        self.car= car

    def set_character(self):
        # get character choice
        self.character= character

    def set_car(self):
        self.car= car

    # accessors for car and character
    def get_character(self):
        return self.character
        
    def get_car(self):
        return self.car
